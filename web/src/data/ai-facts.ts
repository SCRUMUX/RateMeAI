export function getRandomFact(excludeIndex?: number): { fact: AiFact; index: number } {
  let idx: number;
  do {
    idx = Math.floor(Math.random() * AI_FACTS.length);
  } while (idx === excludeIndex && AI_FACTS.length > 1);
  return { fact: AI_FACTS[idx], index: idx };
}
